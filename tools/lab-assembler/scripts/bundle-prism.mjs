// Reads vendor/prism/*.min.js and writes src/prism-vendor.ts with inlined string constants.
// Run automatically as the "prebuild" npm script before tsc.
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dir   = dirname(fileURLToPath(import.meta.url));
const vendor  = join(__dir, '..', 'vendor', 'prism');
const outFile = join(__dir, '..', 'src', 'prism-vendor.ts');

function read(name) {
  return readFileSync(join(vendor, name), 'utf8')
    .replace(/\\/g, '\\\\')   // double backslashes first (covers \1 regex backrefs etc.)
    .replace(/`/g, '\\`')     // then escape backticks
    .replace(/\$/g, '\\$');   // then escape template-literal dollar signs
}

const core = read('prism-core.min.js');

const langs = {
  cpp:        read('prism-c.min.js') + '\n' + read('prism-cpp.min.js'),
  glsl:       read('prism-glsl.min.js'),
  bash:       read('prism-bash.min.js'),
  json:       read('prism-json.min.js'),
  javascript: read('prism-javascript.min.js'),
  typescript: read('prism-javascript.min.js') + '\n' + read('prism-typescript.min.js'),
  python:     read('prism-python.min.js'),
  java:       read('prism-java.min.js'),
  sql:        read('prism-sql.min.js'),
  html:       read('prism-markup.min.js'),
  css:        read('prism-css.min.js'),
};

const entries = Object.entries(langs)
  .map(([k, v]) => `  ${k}: \`${v}\``)
  .join(',\n');

const out = `// Auto-generated by scripts/bundle-prism.mjs — do not edit by hand.
export const PRISM_CORE_JS = \`${core}\`;

export const PRISM_LANG_JS: Record<string, string> = {
${entries},
};
`;

writeFileSync(outFile, out, 'utf8');
console.log('bundle-prism: wrote src/prism-vendor.ts');
