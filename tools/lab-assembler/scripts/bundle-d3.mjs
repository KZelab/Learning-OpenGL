// Reads d3.min.js from node_modules and writes src/d3-vendor.ts with a D3_JS constant.
// Run automatically as part of the "vendor" npm script before tsc.
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dir   = dirname(fileURLToPath(import.meta.url));
const d3File  = join(__dir, '..', 'node_modules', 'd3', 'dist', 'd3.min.js');
const outFile = join(__dir, '..', 'src', 'd3-vendor.ts');

const js = readFileSync(d3File, 'utf8')
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$/g, '\\$');

const out = `// Auto-generated by scripts/bundle-d3.mjs — do not edit by hand.
export const D3_JS = \`${js}\`;
`;

writeFileSync(outFile, out, 'utf8');
console.log('bundle-d3: wrote src/d3-vendor.ts');
