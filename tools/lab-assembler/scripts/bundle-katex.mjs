// Reads KaTeX CSS + woff2 fonts from node_modules, inlines fonts as base64 data URIs,
// strips woff/ttf fallback entries, and writes src/katex-vendor.ts.
// Run automatically as part of the "vendor" npm script before tsc.
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dir   = dirname(fileURLToPath(import.meta.url));
const katexDist = join(__dir, '..', 'node_modules', 'katex', 'dist');
const fontsDir  = join(katexDist, 'fonts');
const outFile   = join(__dir, '..', 'src', 'katex-vendor.ts');

let css = readFileSync(join(katexDist, 'katex.min.css'), 'utf8');

// Inline woff2 fonts as base64 data URIs
css = css.replace(/url\(fonts\/([^)]+\.woff2)\)/g, (_match, filename) => {
  const data = readFileSync(join(fontsDir, filename));
  return `url(data:font/woff2;base64,${data.toString('base64')})`;
});

// Strip woff and ttf fallback src entries (woff2 alone covers all modern browsers)
css = css.replace(/,\s*url\([^)]+\.(woff|ttf)\)\s*format\(["'][^"']*["']\)/g, '');

// Escape for template literal
const escaped = css
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$/g, '\\$');

const out = `// Auto-generated by scripts/bundle-katex.mjs — do not edit by hand.
export const KATEX_CSS = \`${escaped}\`;
`;

writeFileSync(outFile, out, 'utf8');
console.log('bundle-katex: wrote src/katex-vendor.ts');
