Skybox::Skybox()
{
    m_Shader = std::make_unique<Shader>("res/shaders/Skybox.shader");

    glGenVertexArrays(1, &m_VAO);
    glGenBuffers(1, &m_VBO);

    glBindVertexArray(m_VAO);
    glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(s_CubeVertices), s_CubeVertices, GL_STATIC_DRAW);

    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    glBindVertexArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
}

Skybox::~Skybox()
{
    glDeleteVertexArrays(1, &m_VAO);
    glDeleteBuffers(1, &m_VBO);
}

void Skybox::Draw(const glm::mat4& view, const glm::mat4& projection)
{
    // Strip translation so the sky stays "infinitely far"
    glm::mat4 viewRotOnly = glm::mat4(glm::mat3(view));

    glDepthFunc(GL_LEQUAL);
    glDepthMask(GL_FALSE);

    m_Shader->Bind();
    m_Shader->setUniformMat4f("u_View",       viewRotOnly);
    m_Shader->setUniformMat4f("u_Projection", projection);

    glBindVertexArray(m_VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    glDepthMask(GL_TRUE);
    glDepthFunc(GL_LESS);
}
